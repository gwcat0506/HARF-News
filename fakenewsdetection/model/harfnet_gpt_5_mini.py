def resolve_image_path(p: str, data_root: str) -> str:
    p = (p or "").strip()
    if not p:
        return p
    if os.path.isabs(p) and os.path.isfile(p):
        return p
    return os.path.join(os.path.abspath(data_root), p)


def load_dataset(csv_path: str, data_root: str, seed: int = SEED) -> pd.DataFrame:
    """HARFM.csv 로딩 — 이미지+텍스트 둘 다 존재하는 전체 샘플 (멀티모달 전용)"""
    print(f"[ZeroShot] CSV 로딩: {csv_path}")
    df = pd.read_csv(
        csv_path,
        usecols=["final_headline", "image_path", "4_way_label"],
        low_memory=False,
    )
    df = df.dropna(subset=["final_headline", "4_way_label"])
    for c in ["final_headline", "image_path", "4_way_label"]:
        df[c] = df[c].fillna("").astype(str).str.strip()

    # 유효 레이블만
    df = df[df["4_way_label"].isin(CLASS_NAMES)].reset_index(drop=True)
    n_before = len(df)

    # 멀티모달 필터: 이미지 파일 실제 존재 + 헤드라인 비어있지 않음
    print(f"[ZeroShot] 이미지 경로 유효성 검사 중 ({n_before}행)...")

    def is_multimodal(row) -> bool:
        if not row["final_headline"]:
            return False
        rel = row["image_path"]
        if not rel:
            return False
        full = resolve_image_path(rel, data_root)
        return bool(full) and os.path.isfile(full)

    valid_mask = []
    for row in _tqdm(df.itertuples(), total=n_before, desc="  이미지 유효성 검사"):
        valid_mask.append(is_multimodal(row._asdict()))

    df = df[valid_mask].reset_index(drop=True)
    n_after = len(df)

    label_counts = df["4_way_label"].value_counts().to_dict()
    print(f"[ZeroShot] {n_before}행 → 멀티모달 유효 {n_after}행 "
          f"({n_before - n_after}행 스킵)")
    print(f"[ZeroShot] 레이블 분포: {label_counts}")

    if n_after == 0:
        raise ValueError(
            "멀티모달 유효 샘플 없음. --csv_path / --data_root 확인 필요."
        )

    return df


def split_test_indices(labels: List[int], seed: int = SEED) -> List[int]:
    """..."""
    idx = np.arange(len(labels))
    y   = np.array(labels)
    _, te, _, _ = train_test_split(
        idx, y, test_size=TEST_RATIO, stratify=y, random_state=seed,
    )
    return te.tolist()


def preprocess_image_harfnet(img: Image.Image, size: int = IMAGE_SIZE) -> Image.Image:
    """짧은 변 224 리사이즈 후 CenterCrop 224."""
    w, h = img.size
    if w < h:
        new_w, new_h = size, int(round(h * size / w))
    else:
        new_w, new_h = int(round(w * size / h)), size
    img = img.resize((new_w, new_h), Image.LANCZOS)
    left = (new_w - size) // 2
    top  = (new_h - size) // 2
    return img.crop((left, top, left + size, top + size))


def load_image_b64(image_path: str, data_root: str,
                   size: int = IMAGE_SIZE) -> Optional[str]:
    """이미지를"""
    try:
        full = resolve_image_path(image_path, data_root)
        if not full or not os.path.isfile(full):
            return None
        img = preprocess_image_harfnet(Image.open(full).convert("RGB"), size=size)
        buf = io.BytesIO()
        img.save(buf, format="JPEG", quality=90)
        return base64.b64encode(buf.getvalue()).decode("utf-8")
    except Exception as e:
        print(f"  [경고] 이미지 로드 실패 ({image_path}): {e}", file=sys.stderr)
        return None


# ─────────────────────────────────────────────────────────────
# 체크포인트 (중간 저장 / 이어받기)
# ─────────────────────────────────────────────────────────────
class CheckpointManager:
    """완료된 샘플을 JSONL 파일에 한 줄씩 즉시 저장.
    재실행 시 이미 완료된 idx는 자동 스킵.
    """

    def __init__(self, ckpt_path: str):
        self.ckpt_path = ckpt_path
        self._done: Dict[int, dict] = {}  # idx → result
        self._lock = asyncio.Lock()

    def load(self) -> int:
        """기존 체크포인트 로드. 완료된 샘플 수 반환."""
        if not os.path.isfile(self.ckpt_path):
            return 0
        loaded = 0
        with open(self.ckpt_path, "r", encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if not line:
                    continue
                try:
                    r = json.loads(line)
                    self._done[r["idx"]] = r
                    loaded += 1
                except Exception:
                    continue
        return loaded

    def is_done(self, idx: int) -> bool:
        return idx in self._done

    def get_done(self, idx: int) -> Optional[dict]:
        return self._done.get(idx)

    async def save_one(self, result: dict):
        """결과 한 건을 JSONL에 즉시 append (thread-safe)."""
        async with self._lock:
            self._done[result["idx"]] = result
            with open(self.ckpt_path, "a", encoding="utf-8") as f:
                f.write(json.dumps(result, ensure_ascii=False) + "\n")

    def all_results(self, total_idx: List[int]) -> List[dict]:
        """idx 순서대로 결과 반환."""
        return [self._done[i] for i in total_idx if i in self._done]


