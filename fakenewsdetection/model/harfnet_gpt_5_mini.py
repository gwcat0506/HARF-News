def _tqdm_disable() -> bool:
    return os.environ.get("GPT_NO_TQDM", "").lower() in ("1", "true", "yes")


def _tqdm_file():
    """tee(2>&1) 로그에는 progress bar 미기록, 터미널(/dev/tty)에만 표시."""
    if _tqdm_disable():
        return None
    try:
        return open("/dev/tty", "w")
    except OSError:
        return None


class _TqdmNoOp:
    def update(self, n: int = 1) -> None:
        pass

    def close(self) -> None:
        pass


def _tqdm(*args, **kwargs):
    if _tqdm_disable():
        return _TqdmNoOp() if not args else args[0]
    fout = _tqdm_file()
    if fout is None:
        return _TqdmNoOp() if not args else args[0]
    kwargs.setdefault("file", fout)
    kwargs.setdefault("dynamic_ncols", True)
    return tqdm(*args, **kwargs)


