def parse_gpt_response(response_text: str) -> Tuple[str, float, str]:
    """GPT 응답 JSON 파싱 → (label, confidence, reasoning)"""
    try:
        text = response_text.strip()
        if "```" in text:
            text = text.split("```")[1]
            if text.startswith("json"):
                text = text[4:]
        data   = json.loads(text)
        label  = data.get("label", "").strip().upper()
        if label not in CLASS_NAMES:
            for cn in CLASS_NAMES:
                if cn in label:
                    label = cn
                    break
            else:
                label = "HR"
        confidence = float(data.get("confidence", 0.5))
        reasoning  = str(data.get("reasoning", ""))
        return label, confidence, reasoning
    except Exception:
        for cn in CLASS_NAMES:
            if cn in response_text.upper():
                return cn, 0.25, response_text[:300]
        return "HR", 0.25, response_text[:300]


async def call_gpt_async(
    client: AsyncOpenAI,
    headline: str,
    image_b64: str,
    semaphore: asyncio.Semaphore,
    max_retries: int = 5,
    idx: int = 0,
) -> Tuple[str, float, str]:
    """비동기 GPT Vision API 호출.
    Rate Limit(429) 발생 시 지수 백오프로 자동 대기.
    """
    messages = [{"role": "system", "content": SYSTEM_PROMPT}]
    messages += build_user_message(headline, image_b64)

    async with semaphore:
        for attempt in range(max_retries):
            try:
                resp = await client.chat.completions.create(
                    model=MODEL_NAME,
                    messages=messages,
                    max_completion_tokens=MAX_COMPLETION_TOKENS,
                    response_format={"type": "json_object"},
                )
                return parse_gpt_response(resp.choices[0].message.content)

            except Exception as e:
                err_str = str(e).lower()
                # Rate Limit: 더 오래 대기
                if "429" in err_str or "rate" in err_str:
                    wait = min(60, 5 * (2 ** attempt))
                    print(f"\n  [RateLimit] 샘플 {idx} — {wait}s 대기 후 재시도 "
                          f"({attempt+1}/{max_retries})", file=sys.stderr)
                    await asyncio.sleep(wait)
                # 서버 오류: 짧게 대기
                elif "500" in err_str or "502" in err_str or "503" in err_str:
                    wait = 3 * (attempt + 1)
                    print(f"\n  [ServerErr] 샘플 {idx} — {wait}s 대기 "
                          f"({attempt+1}/{max_retries})", file=sys.stderr)
                    await asyncio.sleep(wait)
                else:
                    wait = 2 ** attempt
                    print(f"\n  [Error] 샘플 {idx}: {e} — {wait}s 대기 "
                          f"({attempt+1}/{max_retries})", file=sys.stderr)
                    await asyncio.sleep(wait)

        # 모든 재시도 실패
        print(f"\n  [Fail] 샘플 {idx} 최종 실패", file=sys.stderr)
        return "HR", 0.0, "API_ERROR"


# ─────────────────────────────────────────────────────────────
# 전체 데이터셋 비동기 평가
# ─────────────────────────────────────────────────────────────
async def run_zeroshot_async(
    df: pd.DataFrame,
    data_root: str,
    concurrency: int,
    api_key: str,
    image_size: int,
    ckpt_manager: CheckpointManager,
) -> List[dict]:
    """멀티모달 test split 비동기 zero-shot 평가.
    - 체크포인트에 이미 있는 샘플은 API 호출 없이 스킵
    - 완료된 결과는 즉시 JSONL에 저장 (중단 안전)
    """
    client    = AsyncOpenAI(api_key=api_key)
    semaphore = asyncio.Semaphore(concurrency)

    total     = len(df)
    n_skip    = sum(1 for i in range(total) if ckpt_manager.is_done(i))
    n_todo    = total - n_skip

    print(f"\n[ZeroShot] 평가 시작")
    print(f"  전체 샘플  : {total}")
    print(f"  이미 완료  : {n_skip} (체크포인트 이어받기)")
    print(f"  새로 요청  : {n_todo}")
    print(f"  모델       : {MODEL_NAME}")
    print(f"  이미지     : detail=auto, {image_size}x{image_size} (harfnet CLIP)")
    print(f"  동시 요청  : {concurrency}\n")

    pbar = _tqdm(total=total, initial=n_skip, desc="[ZeroShot] GPT Vision")

    results_by_idx: Dict[int, dict] = {}
    for i in range(total):
        if ckpt_manager.is_done(i):
            results_by_idx[i] = ckpt_manager.get_done(i)

    queue: asyncio.Queue = asyncio.Queue()
    for i, row in df.iterrows():
        if not ckpt_manager.is_done(i):
            queue.put_nowait((int(i), row))

    async def process_one(i: int, row: pd.Series) -> dict:
        image_b64 = load_image_b64(row["image_path"], data_root, size=image_size)
        if image_b64 is None:
            return {
                "idx":        i,
                "headline":   row["final_headline"],
                "image_path": row["image_path"],
                "true_label": row["4_way_label"],
                "pred_label": "HR",
                "confidence": 0.0,
                "reasoning":  "IMAGE_LOAD_ERROR",
                "img_ok":     False,
            }

        pred_label, conf, reasoning = await call_gpt_async(
            client, row["final_headline"], image_b64,
            semaphore, idx=i,
        )
        return {
            "idx":        i,
            "headline":   row["final_headline"],
            "image_path": row["image_path"],
            "true_label": row["4_way_label"],
            "pred_label": pred_label,
            "confidence": conf,
            "reasoning":  reasoning,
            "img_ok":     True,
        }

    async def worker(wid: int):
        while True:
            try:
                i, row = queue.get_nowait()
            except asyncio.QueueEmpty:
                break
            result = await process_one(i, row)
            await ckpt_manager.save_one(result)
            results_by_idx[i] = result
            queue.task_done()
            pbar.update(1)
            if len(results_by_idx) == n_skip + 1:
                print(f"\n[ZeroShot] 첫 API 응답 완료 (worker {wid})", flush=True)

    workers = [asyncio.create_task(worker(w)) for w in range(concurrency)]
    await asyncio.gather(*workers)
    pbar.close()

    results = [results_by_idx[i] for i in range(total)]

    n_err = sum(1 for r in results if not r.get("img_ok", True))
    n_api_err = sum(1 for r in results if r.get("reasoning") == "API_ERROR")
    if n_err or n_api_err:
        print(f"[ZeroShot] 이미지 로드 실패: {n_err}개 | API 오류: {n_api_err}개")

    return list(results)


