def print_cost_estimate(n_samples: int, image_size: int):
    """GPT-5-mini 기준 대략적 비용 추정 (참고용)"""
    # detail=auto, 224x224 (harfnet CLIP) → 약 1 tile 수준
    img_tokens_per   = 255          # 224px 기준 추정
    text_tokens_in   = 350          # system + user 텍스트
    text_tokens_out  = 200          # JSON 응답 (gpt-5-mini reasoning 포함)
    total_in  = (text_tokens_in + img_tokens_per) * n_samples
    total_out = text_tokens_out * n_samples

    # gpt-5-mini 가격 (2025년 기준 추정: input $0.15/1M, output $0.60/1M)
    cost_in  = total_in  / 1_000_000 * 0.15
    cost_out = total_out / 1_000_000 * 0.60
    total_cost = cost_in + cost_out


