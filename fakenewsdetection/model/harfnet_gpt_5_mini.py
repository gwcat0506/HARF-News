def main():
    pa = argparse.ArgumentParser(
        description="HARFNEWS Zero-Shot w/ GPT-5-mini (멀티모달 test 20%)"
    )
    pa.add_argument("--csv_path",   default=DEFAULT_CSV_PATH,  help="HARFM.csv 경로")
