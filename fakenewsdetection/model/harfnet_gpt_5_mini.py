    random.seed(args.seed); np.random.seed(args.seed)

    df_full = load_dataset(args.csv_path, args.data_root, seed=args.seed)
    labels  = [CLASS2IDX[l] for l in df_full["4_way_label"]]
    te_idx  = split_test_indices(labels, seed=args.seed)
    df      = df_full.iloc[te_idx].reset_index(drop=True)
    n_full  = len(df_full)
    n_total = len(df)
