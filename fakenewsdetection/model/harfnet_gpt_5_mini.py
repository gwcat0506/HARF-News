def build_user_message(headline: str, image_b64: str) -> list:
    """멀티모달 메시지 구성 — 이미지 + 헤드라인 항상 함께 전송"""
    text_content = (
        f"Analyze the following news article using BOTH the headline and the image.\n\n"
        f"**Headline:** {headline}\n\n"
        f"Consider: (1) writing style clues for authorship, "
        f"(2) factual plausibility of the claim, "
        f"(3) image authenticity and quality, "
        f"(4) consistency between the image and the headline.\n\n"
        f"Respond ONLY with the JSON object."
    )
    return [
        {
            "role": "user",
            "content": [
                {"type": "text", "text": text_content},
                {
                    "type": "image_url",
                    "image_url": {
                        "url": f"data:image/jpeg;base64,{image_b64}",
                        "detail": "auto",   # 중간 품질 — 이미지 해상도에 맞게 자동 결정
                    },
                },
            ],
        }
    ]


