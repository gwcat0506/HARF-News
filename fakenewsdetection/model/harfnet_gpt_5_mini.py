    api_key = OPENAI_API_KEY or os.environ.get("OPENAI_API_KEY", "")
    if not api_key or api_key.startswith("sk-여기에"):
        print("[오류] 코드 상단 OPENAI_API_KEY에 실제 키를 입력해주세요.", file=sys.stderr)
        sys.exit(1)

