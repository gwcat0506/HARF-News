def compute_metrics(results: List[dict]) -> Dict:
    yt = np.array([CLASS2IDX[r["true_label"]] for r in results])
    yp = np.array([CLASS2IDX.get(r["pred_label"], 0) for r in results])

    proba = np.zeros((len(results), 4), dtype=np.float64)
    for i, r in enumerate(results):
        pred_idx = CLASS2IDX.get(r["pred_label"], 0)
        conf     = float(r["confidence"])
        proba[i, pred_idx] = conf
        rem = (1.0 - conf) / 3.0
        for j in range(4):
            if j != pred_idx:
                proba[i, j] = rem

    acc4  = float(accuracy_score(yt, yp))
    f1_4  = float(f1_score(yt, yp, average="macro", zero_division=0))
    yh, ph = yt // 2, yp // 2
    acc_ha = float(accuracy_score(yh, ph))
    f1_ha  = float(f1_score(yh, ph, average="macro", zero_division=0))
    yr, pr = yt % 2, yp % 2
    acc_rf = float(accuracy_score(yr, pr))
    f1_rf  = float(f1_score(yr, pr, average="macro", zero_division=0))

    try:
        auc_4 = float(roc_auc_score(yt, proba, multi_class="ovr",
                                    average="macro", labels=[0, 1, 2, 3]))
    except Exception:
        auc_4 = float("nan")
    try:
        auc_ha = float(roc_auc_score(yh, proba[:, 2] + proba[:, 3]))
    except Exception:
        auc_ha = float("nan")
    try:
        auc_rf = float(roc_auc_score(yr, proba[:, 1] + proba[:, 3]))
    except Exception:
        auc_rf = float("nan")

    per_class_acc = {}
    for i, cn in enumerate(CLASS_NAMES):
        mask = yt == i
        if mask.sum() > 0:
            per_class_acc[cn] = float((yp[mask] == i).mean())

    return {
        "yt": yt, "yp": yp, "proba": proba,
        "acc4": acc4, "f1_4": f1_4,
        "acc_ha": acc_ha, "f1_ha": f1_ha,
        "acc_rf": acc_rf, "f1_rf": f1_rf,
        "auc_4_ovr_macro": auc_4,
        "auc_ha": auc_ha, "auc_rf": auc_rf,
        "per_class_acc": per_class_acc,
    }


def build_report(results: List[dict], m: Dict, args) -> str:
    yt, yp = m["yt"], m["yp"]
    yh, ph = yt // 2, yp // 2
    yr, pr = yt % 2, yp % 2
    buf = io.StringIO()

    buf.write("=" * 80 + "\n")
    buf.write(f"  HARFNEWS Zero-Shot Evaluation | Model: {args.model_name}\n")
    buf.write(f"  실행 시각  : {datetime.now().isoformat(timespec='seconds')}\n")
    buf.write(f"  모드       : 멀티모달 (이미지+텍스트, max={args.image_size}px, "
              f"batch={args.batch_size})\n")
    buf.write(
        f"  test 샘플  : {len(results)} "
        f"(전체 {getattr(args, 'n_full', '?')} 중 {TEST_RATIO:.0%})\n"
    )
    n_err = sum(1 for r in results if not r.get("img_ok", True))
    n_inf = sum(1 for r in results if r.get("reasoning") == "INFER_ERROR")
    if n_err: buf.write(f"  이미지 오류: {n_err}개\n")
    if n_inf: buf.write(f"  추론 오류  : {n_inf}개\n")
    buf.write("=" * 80 + "\n\n")

    buf.write("=== [1] Human vs AI ===\n")
    buf.write(classification_report(yh, ph, target_names=["Human", "AI"],
                                    digits=4, zero_division=0))
    buf.write(f"\n  Accuracy={m['acc_ha']:.4f}  Macro-F1={m['f1_ha']:.4f}  "
              f"ROC-AUC={m['auc_ha']:.4f}\n\n")

    buf.write("=== [2] Real vs Fake ===\n")
    buf.write(classification_report(yr, pr, target_names=["Real", "Fake"],
                                    digits=4, zero_division=0))
    buf.write(f"\n  Accuracy={m['acc_rf']:.4f}  Macro-F1={m['f1_rf']:.4f}  "
              f"ROC-AUC={m['auc_rf']:.4f}\n\n")

    buf.write("=== [3] 4-Way (HR / HF / AR / AF) ===\n")
    buf.write(classification_report(yt, yp, target_names=CLASS_NAMES,
                                    digits=4, zero_division=0))
    buf.write(f"\n  Accuracy={m['acc4']:.4f}  Macro-F1={m['f1_4']:.4f}  "
              f"ROC-AUC(OVR)={m['auc_4_ovr_macro']:.4f}\n\n")

    buf.write("=== [4] 클래스별 정확도 ===\n")
    for cn, acc in m["per_class_acc"].items():
        buf.write(f"  {cn}: {acc:.4f}\n")

    buf.write("\n" + "=" * 80 + "\n")
    buf.write("★ SUMMARY\n")
    buf.write(f"  4-Way  : F1={m['f1_4']:.4f}  AUC={m['auc_4_ovr_macro']:.4f}\n")
    buf.write(f"  H/A    : F1={m['f1_ha']:.4f}  AUC={m['auc_ha']:.4f}\n")
    buf.write(f"  R/F    : F1={m['f1_rf']:.4f}  AUC={m['auc_rf']:.4f}\n")
    buf.write("=" * 80 + "\n")
    return buf.getvalue()


