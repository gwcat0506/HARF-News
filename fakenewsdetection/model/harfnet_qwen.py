SYSTEM_PROMPT = (
    "You are an expert multimodal news analyst specializing in detecting "
    "AI-generated content and misinformation.\n\n"
    "You will be given a news headline AND its accompanying image. "
    "Analyze BOTH together to classify the article into one of four categories:\n\n"
    "- HR (Human-Real): Written by a human, content is factually true/real\n"
    "- HF (Human-Fake): Written by a human, content is false/fabricated/misleading\n"
    "- AR (AI-Real): Generated by AI, content is factually true/real\n"
    "- AF (AI-Fake): Generated by AI, content is false/fabricated/misleading\n\n"
    "Classification guidelines:\n"
    "[Text/Authorship] AI text: formulaic, syntactically perfect, uniform structure. "
    "Human text: idiosyncratic style, colloquialisms, subtle irregularities.\n"
    "[Text/Veracity] Fake: sensational language, unverifiable claims, logical gaps. "
    "Real: neutral tone, specific verifiable facts.\n"
    "[Image/Authorship] AI image: unnaturally smooth, distorted hands/text/faces, "
    "surreal perfection. Real photo: natural noise, authentic lighting.\n"
    "[Image/Veracity] Mismatch between image context and headline claim. "
    "Manipulated images: inconsistent shadows, blurred edges.\n"
    "[Cross-modal] AF: fluent AI text + AI/manipulated image, factually wrong. "
    "HF: human text + misleading real photo.\n\n"
    "Respond ONLY with a JSON object:\n"
    "{\"label\": \"<HR|HF|AR|AF>\", \"confidence\": <0.0-1.0>, "
    "\"reasoning\": \"<2-3 sentences citing text and image cues>\"}\n"
    "No other text outside the JSON."
)


def build_conversation(headline: str) -> list:
    """Qwen2.5-VL chat 형식 메시지 구성.
    이미지는 processor가 <image> 플레이스홀더로 처리하므로
    content에 {"type": "image"} 항목을 포함.
    """
    user_text = (
        f"Analyze the following news article using BOTH the headline and the image.\n\n"
        f"**Headline:** {headline}\n\n"
        f"Consider: (1) writing style for authorship, "
        f"(2) factual plausibility, "
        f"(3) image authenticity, "
        f"(4) consistency between image and headline.\n\n"
        f"Respond ONLY with the JSON object."
    )
    return [
        {"role": "system", "content": SYSTEM_PROMPT},
        {
            "role": "user",
            "content": [
                {"type": "image"},          # 이미지 플레이스홀더
                {"type": "text", "text": user_text},
            ],
        },
    ]


