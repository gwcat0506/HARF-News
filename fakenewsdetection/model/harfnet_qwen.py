def load_full_multimodal_df(csv_path: str, data_root: str) -> pd.DataFrame:
    """이미지+텍스트 둘 다 존재하는 전체 샘플 로딩"""
    print(f"[Qwen ZeroShot] CSV 로딩: {csv_path}")
    df = pd.read_csv(
        csv_path,
        usecols=["final_headline", "image_path", "4_way_label"],
        low_memory=False,
    )
    df = df.dropna(subset=["final_headline", "4_way_label"])
    for c in ["final_headline", "image_path", "4_way_label"]:
        df[c] = df[c].fillna("").astype(str).str.strip()

    df = df[df["4_way_label"].isin(CLASS_NAMES)].reset_index(drop=True)
    n_before = len(df)
    print(f"[Qwen ZeroShot] 이미지 유효성 검사 중 ({n_before}행)...")

    def is_multimodal(row) -> bool:
        if not row.final_headline:
            return False
        full = resolve_image_path(row.image_path, data_root)
        return bool(full) and os.path.isfile(full)

    mask = [is_multimodal(r) for r in _tqdm(
        df.itertuples(), total=n_before, desc="  이미지 유효성 검사")]
    df = df[mask].reset_index(drop=True)
    n_after = len(df)

    lc = df["4_way_label"].value_counts().to_dict()
    print(f"[Qwen ZeroShot] {n_before}행 → 멀티모달 유효 {n_after}행 "
          f"({n_before - n_after}행 스킵)")
    print(f"[Qwen ZeroShot] 레이블 분포: {lc}")

    if n_after == 0:
        raise ValueError("멀티모달 유효 샘플 없음. --csv_path/--data_root 확인.")
    return df


def split_test_indices(labels: List[int], seed: int = SEED) -> List[int]:
    """..."""
    idx = np.arange(len(labels))
    y   = np.array(labels)
    _, te, _, _ = train_test_split(
        idx, y, test_size=TEST_RATIO, stratify=y, random_state=seed,
    )
    return te.tolist()


