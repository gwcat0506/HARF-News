# ─────────────────────────────────────────────────────────────
class HARFMultimodalDataset(Dataset):
    """(headline, image_path, label, idx) 반환 Dataset.
    이미지 로드 실패 시 회색 더미 이미지로 대체 + img_ok=False 표시.
    """

    def __init__(self, df: pd.DataFrame, data_root: str, image_size: int = IMAGE_SIZE):
        self.df         = df.reset_index(drop=True)
        self.data_root  = os.path.abspath(data_root)
        self.image_size = image_size

    def __len__(self) -> int:
        return len(self.df)

    def _load_image(self, rel_path: str) -> Tuple[Image.Image, bool]:
        try:
            full = resolve_image_path(rel_path, self.data_root)
            if not full or not os.path.isfile(full):
                return Image.new("RGB", (self.image_size, self.image_size),
                                 (127, 127, 127)), False
            img = preprocess_image_harfnet(
                Image.open(full).convert("RGB"), size=self.image_size)
            return img, True
        except Exception:
            return Image.new("RGB", (self.image_size, self.image_size),
                             (127, 127, 127)), False

    def __getitem__(self, idx: int) -> dict:
        row         = self.df.iloc[idx]
        img, img_ok = self._load_image(row["image_path"])
        return {
            "idx":       idx,
            "headline":  row["final_headline"],
            "image":     img,                       # PIL Image (collate에서 처리)
            "true_label": row["4_way_label"],
            "image_path": row["image_path"],
            "img_ok":    img_ok,
        }


def collate_fn(batch: list) -> dict:
    """PIL Image는 리스트로 유지, 나머지는 리스트/텐서로 묶음."""
    return {
        "idx":        [b["idx"]        for b in batch],
        "headline":   [b["headline"]   for b in batch],
        "image":      [b["image"]      for b in batch],   # List[PIL.Image]
        "true_label": [b["true_label"] for b in batch],
        "image_path": [b["image_path"] for b in batch],
        "img_ok":     [b["img_ok"]     for b in batch],
    }


