class CheckpointManager:
    """완료된 샘플을 JSONL에 즉시 저장 / 재실행 시 자동 스킵."""

    def __init__(self, path: str):
        self.path  = path
        self._done: Dict[int, dict] = {}

    def load(self) -> int:
        if not os.path.isfile(self.path):
            return 0
        with open(self.path, "r", encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if not line:
                    continue
                try:
                    r = json.loads(line)
                    self._done[r["idx"]] = r
                except Exception:
                    continue
        return len(self._done)

    def is_done(self, idx: int) -> bool:
        return idx in self._done

    def save_batch(self, results: List[dict]):
        with open(self.path, "a", encoding="utf-8") as f:
            for r in results:
                self._done[r["idx"]] = r
                f.write(json.dumps(r, ensure_ascii=False) + "\n")

    def get(self, idx: int) -> Optional[dict]:
        return self._done.get(idx)

    def all_results(self, n: int) -> List[dict]:
        return [self._done[i] for i in range(n) if i in self._done]


