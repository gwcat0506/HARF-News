def resolve_image_path(p: str, data_root: str) -> str:
    p = (p or "").strip()
    if not p:
        return ""
    if os.path.isabs(p) and os.path.isfile(p):
        return p
    return os.path.join(os.path.abspath(data_root), p)


def preprocess_image_harfnet(img: Image.Image, size: int = IMAGE_SIZE) -> Image.Image:
    """짧은 변 size 리사이즈 후 CenterCrop size."""
    w, h = img.size
    if w < h:
        new_w, new_h = size, int(round(h * size / w))
    else:
        new_w, new_h = int(round(w * size / h)), size
    img = img.resize((new_w, new_h), Image.LANCZOS)
    left = (new_w - size) // 2
    top  = (new_h - size) // 2
    return img.crop((left, top, left + size, top + size))


def set_seed(s: int = SEED):
    random.seed(s)
    np.random.seed(s)
    torch.manual_seed(s)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(s)


def parse_model_response(text: str) -> Tuple[str, float, str]:
    """모델 출력 텍스트 → (label, confidence, reasoning)"""
    try:
        t = text.strip()
        # JSON 블록 추출
        if "```" in t:
            t = t.split("```")[1]
            t = t.lstrip("json").strip()
        # 중괄호 범위만 추출
        start = t.find("{")
        end   = t.rfind("}") + 1
        if start >= 0 and end > start:
            t = t[start:end]
        data  = json.loads(t)
        label = str(data.get("label", "")).strip().upper()
        if label not in CLASS_NAMES:
            for cn in CLASS_NAMES:
                if cn in label:
                    label = cn
                    break
            else:
                label = "HR"
        confidence = float(data.get("confidence", 0.5))
        reasoning  = str(data.get("reasoning", ""))
        return label, confidence, reasoning
    except Exception:
        # 텍스트에서 레이블 직접 탐색
        text_up = text.upper()
        for cn in CLASS_NAMES:
            if cn in text_up:
                return cn, 0.25, text[:300]
        return "HR", 0.25, text[:300]


