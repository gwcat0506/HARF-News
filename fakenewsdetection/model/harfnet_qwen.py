# ─────────────────────────────────────────────────────────────
def load_model_and_processor(
    model_name: str, device: str
) -> Tuple[Qwen2_5_VLForConditionalGeneration, AutoProcessor]:
    """Qwen2.5-VL 모델 및 프로세서 로딩.
    - bfloat16 + Flash Attention 2 자동 적용 (GPU)
    - CPU 폴백 시 float32
    """
    print(f"[Qwen ZeroShot] 모델 로딩: {model_name}")
    use_gpu = device.startswith("cuda") and torch.cuda.is_available()

    load_kw: dict = {
        "trust_remote_code": True,
    }
    if use_gpu:
        load_kw["torch_dtype"]  = torch.bfloat16
        load_kw["device_map"]   = "auto"         # 멀티 GPU 자동 분산
        # Flash Attention 2 사용 가능하면 적용 (없어도 동작)
        try:
            import flash_attn  # noqa: F401
            load_kw["attn_implementation"] = "flash_attention_2"
            print("[Qwen ZeroShot] Flash Attention 2 활성화")
        except ImportError:
            print("[Qwen ZeroShot] Flash Attention 2 미설치 → 기본 attention 사용")
    else:
        load_kw["torch_dtype"] = torch.float32
        load_kw["device_map"]  = "cpu"

    model = Qwen2_5_VLForConditionalGeneration.from_pretrained(
        model_name, **load_kw
    )
    model.eval()

    # 프로세서: 이미지 해상도 범위 설정 (최소 256, 최대 1280)
    processor = AutoProcessor.from_pretrained(
        model_name,
        trust_remote_code=True,
        min_pixels=256 * 28 * 28,
        max_pixels=1280 * 28 * 28,
    )

    gpu_info = ""
    if use_gpu:
        n_gpu = torch.cuda.device_count()
        vram  = sum(torch.cuda.get_device_properties(i).total_memory
                    for i in range(n_gpu)) / 1024**3
        gpu_info = f" | GPU×{n_gpu} ({vram:.1f} GB)"
    print(f"[Qwen ZeroShot] 모델 준비 완료{gpu_info}")
    return model, processor


# ─────────────────────────────────────────────────────────────
# 배치 추론
# ─────────────────────────────────────────────────────────────
@torch.inference_mode()
def infer_batch(
    model: Qwen2_5_VLForConditionalGeneration,
    processor: AutoProcessor,
    headlines: List[str],
    images: List[Image.Image],
    device: str,
    max_new_tokens: int = 256,
) -> List[Tuple[str, float, str]]:
    """하나의 배치에 대해 Qwen2.5-VL 추론 수행.
    반환: [(label, confidence, reasoning), ...]
    """
    # 각 샘플의 conversation 템플릿 구성
    conversations = [build_conversation(h) for h in headlines]

    # chat template 적용 → 텍스트 프롬프트 생성
    texts = [
        processor.apply_chat_template(
            conv,
            tokenize=False,
            add_generation_prompt=True,
        )
        for conv in conversations
    ]

    # 프로세서로 토큰화 + 이미지 전처리
    # padding=True: 배치 내 길이 통일
    inputs = processor(
        text=texts,
        images=images,
        padding=True,
        return_tensors="pt",
    )

    # GPU로 이동
    target_device = next(model.parameters()).device
    inputs = {k: v.to(target_device) for k, v in inputs.items()}

    # 생성
    generated_ids = model.generate(
        **inputs,
        max_new_tokens=max_new_tokens,
        do_sample=False,         # greedy (zero-shot 재현성)
        temperature=None,
        top_p=None,
        pad_token_id=processor.tokenizer.eos_token_id,
    )

    # 입력 토큰 제거 → 생성된 부분만 추출
    input_len = inputs["input_ids"].shape[1]
    output_ids = generated_ids[:, input_len:]

    # 디코딩
    decoded = processor.batch_decode(
        output_ids,
        skip_special_tokens=True,
        clean_up_tokenization_spaces=False,
    )

    return [parse_model_response(text) for text in decoded]


# ─────────────────────────────────────────────────────────────
# 전체 평가 루프
# ─────────────────────────────────────────────────────────────
def run_evaluation(
    model: Qwen2_5_VLForConditionalGeneration,
    processor: AutoProcessor,
    df: pd.DataFrame,
    data_root: str,
    device: str,
    args,
    ckpt: CheckpointManager,
) -> List[dict]:
    """전체 멀티모달 데이터셋 배치 평가."""

    dataset    = HARFMultimodalDataset(df, data_root, image_size=args.image_size)
    dataloader = DataLoader(
        dataset,
        batch_size=args.batch_size,
        shuffle=False,          # 순서 유지 (체크포인트 idx 기준)
        num_workers=args.num_workers,
        collate_fn=collate_fn,
        pin_memory=device.startswith("cuda"),
    )

    n_total = len(df)
    n_done  = sum(1 for i in range(n_total) if ckpt.is_done(i))
    n_todo  = n_total - n_done

    print(f"\n[Qwen ZeroShot] 평가 시작")
    print(f"  test 샘플  : {n_total}")
    print(f"  이미 완료  : {n_done} (체크포인트 이어받기)")
    print(f"  새로 처리  : {n_todo}")
    print(f"  배치 크기  : {args.batch_size}")
    print(f"  이미지 크기: {args.image_size}x{args.image_size} (harfnet CLIP)\n")

    pbar = _tqdm(total=n_total, initial=n_done, desc="[Qwen ZeroShot] 추론")

    for batch in dataloader:
        idxs      = batch["idx"]          # List[int]
        headlines = batch["headline"]      # List[str]
        images    = batch["image"]         # List[PIL.Image]
        labels    = batch["true_label"]    # List[str]
        paths     = batch["image_path"]    # List[str]
        img_oks   = batch["img_ok"]        # List[bool]

        # 이미 완료된 샘플이 포함된 배치는 건너뜀
        # 미완료 샘플만 추려서 추론 (부분 배치)
        todo_pos = [i for i, idx in enumerate(idxs) if not ckpt.is_done(idx)]

        if not todo_pos:
            pbar.update(len(idxs))
            continue

        # 미완료 샘플만 슬라이싱
        todo_idx      = [idxs[i]      for i in todo_pos]
        todo_headlines= [headlines[i]  for i in todo_pos]
        todo_images   = [images[i]     for i in todo_pos]
        todo_labels   = [labels[i]     for i in todo_pos]
        todo_paths    = [paths[i]      for i in todo_pos]
        todo_img_oks  = [img_oks[i]    for i in todo_pos]

        # 추론
        try:
            preds = infer_batch(
                model, processor,
                todo_headlines, todo_images,
                device, args.max_new_tokens,
            )
        except torch.cuda.OutOfMemoryError:
            print("\n[OOM] VRAM 부족 — --batch_size를 줄이거나 --image_size를 낮추세요.",
                  file=sys.stderr)
            torch.cuda.empty_cache()
            # 1개씩 재시도
            preds = []
            for h, img in zip(todo_headlines, todo_images):
                try:
                    p = infer_batch(model, processor, [h], [img],
                                    device, args.max_new_tokens)
                    preds.extend(p)
                except Exception as e:
                    print(f"  [Fail] {e}", file=sys.stderr)
                    preds.append(("HR", 0.0, "INFER_ERROR"))

        # 결과 저장
        batch_results = []
        for pos, (pred_label, conf, reasoning) in enumerate(preds):
            batch_results.append({
                "idx":        todo_idx[pos],
                "headline":   todo_headlines[pos],
                "image_path": todo_paths[pos],
                "true_label": todo_labels[pos],
                "pred_label": pred_label,
                "confidence": conf,
                "reasoning":  reasoning,
                "img_ok":     todo_img_oks[pos],
            })

        ckpt.save_batch(batch_results)
        pbar.update(len(idxs))

    pbar.close()

    results = ckpt.all_results(n_total)
    n_err   = sum(1 for r in results if not r.get("img_ok", True))
    n_inf_err = sum(1 for r in results if r.get("reasoning") == "INFER_ERROR")
    if n_err or n_inf_err:
        print(f"[Qwen ZeroShot] 이미지 로드 실패: {n_err}개 | "
              f"추론 오류: {n_inf_err}개")
    return results


