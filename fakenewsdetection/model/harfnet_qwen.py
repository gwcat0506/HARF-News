def main():
    pa = argparse.ArgumentParser(
        description="HARFNEWS Zero-Shot w/ Qwen2.5-VL (멀티모달 test 20%)"
    )
    pa.add_argument("--csv_path",      default=DEFAULT_CSV_PATH)
    pa.add_argument("--data_root",     default=DEFAULT_DATA_ROOT)
    pa.add_argument("--output_dir",    default=DEFAULT_OUTPUT)
    pa.add_argument("--model_name",    default=MODEL_NAME_DEFAULT,
                    help="HuggingFace 모델 ID")
    pa.add_argument("--batch_size",    type=int, default=4,
                    help="배치 크기 (VRAM 24GB 기준 기본값 4)")
    pa.add_argument("--max_new_tokens",type=int, default=256)
    pa.add_argument("--image_size",    type=int, default=IMAGE_SIZE,
                    help="이미지 크기 (기본: 224,")
    pa.add_argument("--num_workers",   type=int, default=4)
    pa.add_argument("--device",        default="cuda" if torch.cuda.is_available() else "cpu")
    pa.add_argument("--seed",          type=int, default=SEED)
    pa.add_argument("--no_resume",     action="store_true",
                    help="체크포인트 무시하고 처음부터 재실행")
    args = pa.parse_args()

    set_seed(args.seed)
    os.makedirs(args.output_dir, exist_ok=True)

    # GPU 정보 출력
    if torch.cuda.is_available():
        for i in range(torch.cuda.device_count()):
            p = torch.cuda.get_device_properties(i)
            print(f"[GPU {i}] {p.name} | VRAM {p.total_memory/1024**3:.1f} GB")
    else:
        print("[경고] CUDA 미감지 — CPU로 실행 (매우 느림)")

    df_full = load_full_multimodal_df(args.csv_path, args.data_root)
    labels  = [CLASS2IDX[l] for l in df_full["4_way_label"]]
    te_idx  = split_test_indices(labels, seed=args.seed)
    df      = df_full.iloc[te_idx].reset_index(drop=True)
    n_full  = len(df_full)
    args.n_full = n_full
    print(f"[Qwen ZeroShot] test split: {len(df)}/{n_full} "
          f"({TEST_RATIO:.0%}, seed={args.seed})")

    # 체크포인트
    ckpt_path = os.path.join(args.output_dir, "qwen_zeroshot_checkpoint.jsonl")
    ckpt      = CheckpointManager(ckpt_path)
    if args.no_resume and os.path.isfile(ckpt_path):
        print(f"[Qwen ZeroShot] --no_resume: 기존 체크포인트 삭제")
        os.remove(ckpt_path)
    n_done = ckpt.load()
    if n_done > 0:
        print(f"[Qwen ZeroShot] 체크포인트 복원: {n_done}/{len(df)}개 완료")

    # 모델 로딩
    model, processor = load_model_and_processor(args.model_name, args.device)

    # 평가
    start   = time.time()
    results = run_evaluation(model, processor, df, args.data_root,
                             args.device, args, ckpt)
    elapsed = time.time() - start

    results.sort(key=lambda r: r["idx"])
    print(f"\n[Qwen ZeroShot] 완료 — {elapsed:.1f}초 ({elapsed/60:.1f}분) | "
          f"샘플당 {elapsed/max(len(results),1):.2f}초")

    # 메트릭 & 리포트
    m      = compute_metrics(results)
    report = build_report(results, m, args)
    print("\n" + report)

    # 저장
    ts     = datetime.now().strftime("%Y%m%d_%H%M%S")
    prefix = f"qwen_zeroshot_multimodal_test20_{ts}"

    rp = os.path.join(args.output_dir, f"{prefix}_report.txt")
    with open(rp, "w", encoding="utf-8") as f:
        f.write(report)
    print(f"[저장] 리포트     : {rp}")

    detail_df = pd.DataFrame([{
        "idx":        r["idx"],
        "headline":   r["headline"],
        "image_path": r["image_path"],
        "true_label": r["true_label"],
        "pred_label": r["pred_label"],
        "confidence": r["confidence"],
        "correct":    r["true_label"] == r["pred_label"],
        "ha_correct": (CLASS2IDX[r["true_label"]] // 2) == (CLASS2IDX.get(r["pred_label"], 0) // 2),
        "rf_correct": (CLASS2IDX[r["true_label"]] % 2) == (CLASS2IDX.get(r["pred_label"], 0) % 2),
        "img_ok":     r.get("img_ok", True),
        "reasoning":  r["reasoning"],
    } for r in results])
    cp = os.path.join(args.output_dir, f"{prefix}_predictions.csv")
    detail_df.to_csv(cp, index=False, encoding="utf-8-sig")
    print(f"[저장] 예측 CSV   : {cp}")

    metrics_out = {k: v for k, v in m.items() if k not in ("yt", "yp", "proba")}
    metrics_out.update({
        "model":        args.model_name,
        "n_samples":    len(results),
        "n_full":       n_full,
        "test_ratio":   TEST_RATIO,
        "batch_size":   args.batch_size,
        "image_size":   args.image_size,
        "elapsed_sec":  round(elapsed, 2),
    })
    jp = os.path.join(args.output_dir, f"{prefix}_metrics.json")
    with open(jp, "w", encoding="utf-8") as f:
        json.dump(metrics_out, f, ensure_ascii=False, indent=2)
    print(f"[저장] 메트릭 JSON: {jp}")
    print(f"[저장] 체크포인트  : {ckpt_path}")

    print(f"\n{'='*60}")
    print(f"[Qwen2.5-VL Zero-Shot | 멀티모달 test 20%] HARFNEWS 평가 결과")
    print(f"  test 샘플 : {len(results)} / {n_full} ({TEST_RATIO:.0%})")
    print(f"  4-Way  F1 = {m['f1_4']:.4f}   AUC = {m['auc_4_ovr_macro']:.4f}")
    print(f"  H/A    F1 = {m['f1_ha']:.4f}   AUC = {m['auc_ha']:.4f}")
    print(f"  R/F    F1 = {m['f1_rf']:.4f}   AUC = {m['auc_rf']:.4f}")
    print(f"{'='*60}")


